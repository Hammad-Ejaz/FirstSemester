void loadMaze()
{
    fstream file;
    string line;
    file.open("mario.txt" , ios::in);
    getline(file,line);
    score = stoi(line);
    getline(file,line);
    lives = stoi(line);
    getline(file,line);
    c = stoi(line);
    while(!file.eof())
    {
        for (int row = 0; row < 42; row++)
        {
            getline(file,line);
            for (int col = 0; col < 852 ; col++)
            {
                maze[row][col] = line[col];
            }

        }
    }
    file.close();
}
